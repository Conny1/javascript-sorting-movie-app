const content = document.querySelector('.content');


const btnContainer = document.querySelector('.btn-container');


window.addEventListener('DOMContentLoaded',()=>{
   
    loadcontent(movies)
    // console.log(btns);
   const dynamicbtn = movies.reduce((values,item)=>{

    if (!values.includes(item.category)) {

        values.push(item.category);
        
    }
    return values;


   },['All'])

  let buttons = dynamicbtn.map(btn=>{
    
    return  `
    <button data-id=${btn} class=" btn">${btn}</button>
    `

   }).join("");

console.log(buttons);
btnContainer.innerHTML = buttons;
const btns =  document.querySelectorAll('.btn');
btns.forEach(btn => {
    
    btn.addEventListener('click', (e)=>{
        // console.log(e.currentTarget.dataset.id);
      let  categories = e.currentTarget.dataset.id;
       
// filter the array based on datasets
        let categoryitem = movies.filter((item)=>{
            if(categories === item.category){
                return item
            }

        });
        // console.log(categoryitem);   
        if(categories === 'All'){
            loadcontent(movies);
        }else{
            loadcontent(categoryitem);
        }    
    });
    
});
   
});
 
// function for loading content into the window
function loadcontent(arr){
    // console.log("hello");
    let items = arr.map((item)=>{
        // console.log(item)
        return`
        <article class="movie-container">
        <div class="img-container"><img src=${item.img} alt=${item.Name} /></div>
        <div class="description">
          <p class="movie-name">Name: ${item.Name}</p>
          <hr/>
          <p class="category">category: ${item.category}</p>
          <hr/>
          <p class="movie-rating">imdb rating: ${item.rating}</p>
          <hr/>
          <p class="decription">
          ${item.description}
          </p>
        </div>
      </article> `

    })
   content.innerHTML= items.join('');
   
}